def squote(string):
    """suround a string with single quotes"""
    return "'"+ string +"'"

def dquote(string):
    """suround a string with double quotes"""
    return '"'+ string +'"'

def parenthesis(string):
    """ suround a string with parenthesis """
    return '('+string+')'